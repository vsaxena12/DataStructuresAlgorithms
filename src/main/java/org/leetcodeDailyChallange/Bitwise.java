package org.leetcodeDailyChallange;

public class Bitwise {

    public static void main(String[] args) {
        int value = 2<<1;
        System.out.println(value);

        value = 2<<2;
        System.out.println(value);

        value = 2<<3;
        System.out.println(value);

        value = 2<<4;
        System.out.println(value);

        value = 2>>1;
        System.out.println(value);

        value = 2>>2;
        System.out.println(value);

        value = 2>>3;
        System.out.println(value);

        value = -8>>3;
        System.out.println(value);

        int n = ~5;
        System.out.println(n);
    }
}
